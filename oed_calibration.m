% 1. Calcular el parámetro 'a'
phi_c_rad = deg2rad(phi_c); % Convertir a radianes
a = (sqrt(3) * (3 - sin(phi_c_rad))) / (2 * sqrt(2) * sin(phi_c_rad));

% 2. Calcular f_d0
fd1 = (e01 - ed0) / (ec0 - ed0);
fd2 = (e02 - ed0) / (ec0 - ed0);

% Calcular módulos de rigidez incrementales (condición suelta)
dp1 = diff(p1);
de1 = diff(e1);
dp6 = diff(p6);
