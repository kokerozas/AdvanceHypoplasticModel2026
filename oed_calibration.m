% Para condición densa
dp6 = diff(p6); % Diferencia de presiones
de6 = diff(e6); % Diferencia de vacíos
E2_mod = mean(dp6 ./ de6); % Promedio de las diferencias

% 4. Calcular bxeta_0 (ec. 24)
beta_0 = (3 + a^2 - a * sqrt(3) * fd1) / (3 + a^2 - a * sqrt(3) * fd2);

% Definir un rango común de presiones entre p1 y p6
